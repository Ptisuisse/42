#include "libft.h"

long int	ft_atol(const char *str)
{
	long int	j;
	long int	k;

	j = 1;
	k = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == 43 || *str == 45)
	{
		if (*str == 45)
			j = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		k = k * 10 + (*str - '0');
		str++;
	}
	return (k * j);
}
