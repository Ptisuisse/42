#include <ctype.h>
#include <stdio.h>

int	ft_tolower(int c)
{
	if (c >= 65 && c <= 90)
		c += 32;
	return (c);
}

int	main(void)
{
	char	c = 'A';
	printf("%c", ft_tolower(c));
}
