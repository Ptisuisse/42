#include <ctype.h>
#include <stdio.h>

int	ft_toupper(int c)
{
	if (c >= 65 && c <= 90)
		c += 32;
	if (c >= 97 && c <= 122)
		c -= 32;
	return (c);
}

int	main(void)
{
	char	c = 'a';
	printf("%c", ft_toupper(c));
}
